package lab4_Inheritance_Polymorphism_Ex_1;

public class Person extends Account
{
	String name;
	float age;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public float getAge() {
		return age;
	}
	public void setAge(float age) {
		this.age = age;
	}
	
	
}
